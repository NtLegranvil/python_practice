"""
Calculate the Square Footage of a room
"""

# This program computes the square footage of a room
x = float(input("Input length: ")) # user can input length into variable
y = float(input("Input Width: ")) # user can input width into variable
sqf = (x*y) # square footage formula L x W = A (in square feet)
print("The Square Footage of the room is:", sqf) # outputs string with proper calculation
